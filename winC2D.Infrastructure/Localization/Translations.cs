    public static string Get(string langCode, string key)
    {
        // exact match
        if (_table.TryGetValue(langCode, out var dict) && dict.TryGetValue(key, out var val))
            return val;

        // try base language (e.g. "zh" for "zh-CN")
        var dash = langCode.IndexOf('-');
        if (dash > 0)
        {
            var baseLang = langCode[..dash];
            if (_table.TryGetValue(baseLang, out var baseDict) && baseDict.TryGetValue(key, out var baseVal))
                return baseVal;
        }

        // fall back to English
        if (_table.TryGetValue("en", out var en) && en.TryGetValue(key, out var enVal))
            return enVal;

        // ultimate fallback: return the key itself
        return key;
    }
